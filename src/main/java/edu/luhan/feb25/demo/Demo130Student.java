package edu.luhan.feb25.demo;

public class Demo130Student {

    private String name;
    private int age;

    public Demo130Student() {
    }

    public Demo130Student(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
